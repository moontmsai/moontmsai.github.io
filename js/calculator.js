const calculator = {
    add: function (a, b) {
        console.log(a + b);
    },
    minus: function (a, b) {
        console.log(a - b);
    },
    div: function (a , b) {
        console.log(a / b);
    },
    power: function (a , b) {
        console.log(a ** b);
    },
};

calculator.add(10, 2);
calculator.minus(10, 2);
calculator.div(10, 2);
calculator.power(10, 2);